//draw the text to the cube one letter after another with a travelling effect 
void drawText(CRGB pixels[NUM_LEDS], String text, uint8_t initialHue, uint8_t deltaHue, int delayTime) {
    //use the setLed function to draw the text
    //each letter starts in the back and moves to the front
    //when it reaches the front, the next letter starts

    FastLED.setBrightness(255);
    
    int currentLetterIndex = 0;
    int currentLetterMatixIndex = 0;
    char currentLetter = text[currentLetterIndex];

    if (currentLetter >= 48 && currentLetter <= 57) {
        currentLetterMatixIndex = int(currentLetter) - 48;
    }
    else if (currentLetter >= 65 && currentLetter <= 90) {
        currentLetterMatixIndex = int(currentLetter) - 65 + 10;
    }
    else if (currentLetter == ':') {
        currentLetterMatixIndex = 36; // index of CHAR_COLON in LETTERS array
    }

    

    for (unsigned int i = 0; i < text.length(); i++) {
        for (int y = 9; y > -1; y--) {
            for (int x = 9; x > -1; x--) {
                for (int z = 0; z < 10; z++) {
                    if ((*LETTERS[currentLetterMatixIndex])[9 - z][x] == 1) {
                        setLed(x, y, z, CHSV(initialHue, 255, 255));
                    }     
                }
            }

            fadeToBlackBy(pixels, NUM_LEDS, 100);
            
            FastLED.show();
            delay(delayTime);
            
        }
        initialHue += deltaHue;
        currentLetterIndex++;
        currentLetter = text[currentLetterIndex];
        if (currentLetter >= 48 && currentLetter <= 57) {
            currentLetterMatixIndex = int(currentLetter) - 48;
        }
        else {
            currentLetterMatixIndex = int(currentLetter) - 65 + 10;
        }
    }

    unsigned long startMillis = millis();
    while (millis() - startMillis < 500) {
        
        fadeToBlackBy(pixels, NUM_LEDS, 100);
        FastLED.show();
    }


}




